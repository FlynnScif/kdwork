package com.kingdee.drc.entity;

public class ProjectInfo {
	private String projectNo;
	private String projectName;
	private String conDays;
	private String impAmount;

	public String getProjectNo() {
		return projectNo;
	}

	public void setProjectNo(String projectNo) {
		this.projectNo = projectNo;
	}

	public String getProjectName() {
		return projectName;
	}

	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}

	public String getConDays() {
		return conDays;
	}

	public void setConDays(String conDays) {
		this.conDays = conDays;
	}

	public String getImpAmount() {
		return impAmount;
	}

	public void setImpAmount(String impAmount) {
		this.impAmount = impAmount;
	}
}
